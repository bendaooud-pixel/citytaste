export function getPlacesByCity(citySlug: string): Place[] {
  return places.filter((p) => p.citySlug === citySlug);
}

export function getPlaceBySlug(citySlug: string, placeSlug: string): Place | undefined {
  return places.find((p) => p.citySlug === citySlug && p.slug === placeSlug);
}

